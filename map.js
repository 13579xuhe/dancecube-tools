// ---------- 获取单个机台在线状态 ----------
async function fetchMachineStatus(machineId) {
    const statusUrl = `${STATUS_API_URL}?machineId=${machineId}`;
    try {
        const response = await fetch(statusUrl, {
            method: 'GET',
            headers: { 'Content-Type': 'application/json' }
        });

        if (!response.ok) {
            console.warn(`状态API响应错误: ${response.status} for machineId ${machineId}`);
            return false;
        }

        const data = await response.json();
        const isOnline = data.Online === true;
        return isOnline;
    } catch (error) {
        console.error(`获取机台 ${machineId} 状态失败:`, error);
        return false;
    }
}

// ---------- 批量获取机台在线状态（带缓存）----------
const statusCache = new Map();
const CACHE_TTL = 60000;

async function fetchMachineStatusWithCache(machineId) {
    const cached = statusCache.get(machineId);
    if (cached && Date.now() - cached.time < CACHE_TTL) {
        return cached.status;
    }
    const status = await fetchMachineStatus(machineId);
    statusCache.set(machineId, { status, time: Date.now() });
    return status;
}

// ---------- 调用真实 API ----------
async function fetchMachinesByCity(lng, lat, cityName) {
    const url = `${API_URL}?lng=${lng}&lat=${lat}`;
    console.log('🌐 请求API:', url);

    try {
        const response = await fetch(url, {
            method: 'GET',
            headers: { 'Content-Type': 'application/json' }
        });

        if (!response.ok) {
            throw new Error(`HTTP ${response.status}: ${response.statusText}`);
        }

        const data = await response.json();
        console.log('✅ API返回数据:', data);

        let machines = [];
        if (data && Array.isArray(data)) {
            machines = data;
        } else if (data && data.data && Array.isArray(data.data)) {
            machines = data.data;
        } else {
            console.warn('⚠️ API返回数据格式异常:', data);
            return [];
        }

        const parsedMachines = machines.map((item, idx) => {
            let province = "未知", city = "未知";
            const provinceCity = item.ProvinceAndCity || item.provinceAndCity || "";
            if (provinceCity) {
                const parts = provinceCity.split(/\s+/);
                if (parts.length >= 1) province = parts[0];
                if (parts.length >= 2) city = normalizeCityName(parts[1]) || parts[1];
            }

            if (city === "未知" && cityName) city = cityName;

            let lat = parseFloat(item.纬度 || item.lat || item.Latitude || 0);
            let lng = parseFloat(item.经度 || item.lng || item.Longitude || 0);

            if ((!lat || lat === 0) && cityName && cityCoordinates[cityName]) {
                lng = cityCoordinates[cityName][0];
                lat = cityCoordinates[cityName][1];
            }

            const machineId = item.MachineTerminalID || item.machineId || item.id || `machine_${idx}`;
            // MachineType: 1 = 秀机/Show机, 0 = 一代/二代机器
            const machineType = item.MachineType !== undefined ? item.MachineType : (item.machineType !== undefined ? item.machineType : 0);

            return {
                id: machineId,
                name: item.PlaceName || "舞立方机台",
                province: province,
                city: city,
                address: item.Address || "",
                lat: lat,
                lng: lng,
                status: "unknown",
                machineType: machineType
            };
        }).filter(item => item.lat && item.lng && item.lat !== 0 && item.lng !== 0);

        if (parsedMachines.length === 0) {
            console.log('⚠️ 没有找到有效的机台数据');
            return [];
        }

        console.log(`🔍 正在获取 ${parsedMachines.length} 台机台的在线状态...`);

        const statusPromises = parsedMachines.map(async (machine) => {
            const isOnline = await fetchMachineStatusWithCache(machine.id);
            machine.status = isOnline ? "active" : "maintenance";
            return machine;
        });

        const finalMachines = await Promise.all(statusPromises);

        const onlineCount = finalMachines.filter(m => m.status === "active").length;
        const showCount = finalMachines.filter(m => m.machineType === 1).length;

        console.log(`✅ 共 ${finalMachines.length} 台机台，在线: ${onlineCount} 台，秀机: ${showCount} 台`);

        return finalMachines;

    } catch (error) {
        console.error('❌ API调用失败:', error);
        return [];
    }
}

// ---------- 根据城市名称获取机台数据 ----------
async function loadCabinetsDataByCity(cityName) {
    const normalizedCity = normalizeCityName(cityName) || cityName;

    const coords = cityCoordinates[normalizedCity];
    if (!coords) {
        console.error(`❌ 未找到城市 ${normalizedCity} 的坐标`);
        return [];
    }
    const [lng, lat] = coords;
    console.log(`🔍 正在获取 ${normalizedCity} (${lng}, ${lat}) 的机台数据...`);

    try {
        const data = await fetchMachinesByCity(lng, lat, normalizedCity);
        currentCabinetsData = data;
        return data;
    } catch (error) {
        console.error('❌ 加载机台数据失败:', error);
        return [];
    }
}

function getCabinetsData() {
    return currentCabinetsData;
}

